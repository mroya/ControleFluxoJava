public class ExemploForArray {
    public static void main(String[] args) {
        String alunos[] = { "Felipe", "Jonas", "Julia", "Marcos" };
        System.out.println();
        for (int i = 0; i < alunos.length; i++) {
            System.out.println("Nome do Aluno na Posição " + i + " é " + alunos[i]);
        }
        System.out.println();
    }
}
